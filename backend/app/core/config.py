"""
Purpose: Load and validate all application settings from environment variables and .env file

Structure:
    Settings (class): config - Pydantic settings with validation for DB, auth, email, CORS
    parse_cors (func): config - Parse CORS origins from comma-separated string or list
    settings (Settings): output - Singleton settings instance

Relationships:
    Consumes: ../.env file, environment variables
    Produces: settings (consumed by all backend modules)

Semantics:
    Domain: configuration
    Logic: [Warns on default secrets in local, raises in staging/production,
            SQLALCHEMY_DATABASE_URI computed from POSTGRES_* fields,
            emails_enabled computed from SMTP_HOST + EMAILS_FROM_EMAIL]

Important:
    SECRET_KEY, POSTGRES_PASSWORD, and FIRST_SUPERUSER_PASSWORD MUST NOT be
    "changethis" in staging/production — validation will raise ValueError.
"""

import secrets
import warnings
from typing import Annotated, Any, Literal

from pydantic import (
    AnyUrl,
    BeforeValidator,
    EmailStr,
    HttpUrl,
    PostgresDsn,
    computed_field,
    model_validator,
)
from pydantic_settings import BaseSettings, SettingsConfigDict
from typing_extensions import Self


def parse_cors(v: Any) -> list[str] | str:
    """Purpose: Parse CORS origins from comma-separated string or list"""
    if isinstance(v, str) and not v.startswith("["):
        return [i.strip() for i in v.split(",") if i.strip()]
    elif isinstance(v, list | str):
        return v
    raise ValueError(v)


class Settings(BaseSettings):
    """
    Purpose: Centralized application configuration with env var loading and validation

    Structure:
        API_V1_STR (str): config - API version prefix
        SECRET_KEY (str): config - JWT signing key
        SQLALCHEMY_DATABASE_URI (computed): config - Full PostgreSQL connection URI
        emails_enabled (computed): config - Whether email sending is configured
        all_cors_origins (computed): config - Combined CORS + frontend origins

    Important:
        env_file points to "../.env" (one level above backend/).
        Default secrets trigger warnings locally, errors in staging/production.
    """
    model_config = SettingsConfigDict(
        # Use top level .env file (one level above ./backend/)
        env_file="../.env",
        env_ignore_empty=True,
        extra="ignore",
    )
    API_V1_STR: str = "/api/v1"
    SECRET_KEY: str = secrets.token_urlsafe(32)
    # 60 minutes * 24 hours * 8 days = 8 days
    ACCESS_TOKEN_EXPIRE_MINUTES: int = 60 * 24 * 8
    FRONTEND_HOST: str = "http://localhost:5173"
    ENVIRONMENT: Literal["local", "staging", "production"] = "local"

    BACKEND_CORS_ORIGINS: Annotated[
        list[AnyUrl] | str, BeforeValidator(parse_cors)
    ] = []

    @computed_field  # type: ignore[prop-decorator]
    @property
    def all_cors_origins(self) -> list[str]:
        """Purpose: Combine BACKEND_CORS_ORIGINS and FRONTEND_HOST into unified origin list"""
        return [str(origin).rstrip("/") for origin in self.BACKEND_CORS_ORIGINS] + [
            self.FRONTEND_HOST
        ]

    PROJECT_NAME: str
    SENTRY_DSN: HttpUrl | None = None
    POSTGRES_SERVER: str
    POSTGRES_PORT: int = 5432
    POSTGRES_USER: str
    POSTGRES_PASSWORD: str = ""
    POSTGRES_DB: str = ""

    @computed_field  # type: ignore[prop-decorator]
    @property
    def SQLALCHEMY_DATABASE_URI(self) -> PostgresDsn:
        """Purpose: Build PostgreSQL connection URI from individual POSTGRES_* settings"""
        return PostgresDsn.build(
            scheme="postgresql+psycopg",
            username=self.POSTGRES_USER,
            password=self.POSTGRES_PASSWORD,
            host=self.POSTGRES_SERVER,
            port=self.POSTGRES_PORT,
            path=self.POSTGRES_DB,
        )

    SMTP_TLS: bool = True
    SMTP_SSL: bool = False
    SMTP_PORT: int = 587
    SMTP_HOST: str | None = None
    SMTP_USER: str | None = None
    SMTP_PASSWORD: str | None = None
    EMAILS_FROM_EMAIL: EmailStr | None = None
    EMAILS_FROM_NAME: str | None = None

    @model_validator(mode="after")
    def _set_default_emails_from(self) -> Self:
        """Purpose: Default EMAILS_FROM_NAME to PROJECT_NAME if not set"""
        if not self.EMAILS_FROM_NAME:
            self.EMAILS_FROM_NAME = self.PROJECT_NAME
        return self

    EMAIL_RESET_TOKEN_EXPIRE_HOURS: int = 48

    @computed_field  # type: ignore[prop-decorator]
    @property
    def emails_enabled(self) -> bool:
        """Purpose: Check if SMTP is configured (SMTP_HOST and EMAILS_FROM_EMAIL both set)"""
        return bool(self.SMTP_HOST and self.EMAILS_FROM_EMAIL)

    EMAIL_TEST_USER: EmailStr = "test@example.com"
    FIRST_SUPERUSER: EmailStr
    FIRST_SUPERUSER_PASSWORD: str

    def _check_default_secret(self, var_name: str, value: str | None) -> None:
        """Purpose: Warn (local) or raise (staging/production) if secret is still 'changethis'"""
        if value == "changethis":
            message = (
                f'The value of {var_name} is "changethis", '
                "for security, please change it, at least for deployments."
            )
            if self.ENVIRONMENT == "local":
                warnings.warn(message, stacklevel=1)
            else:
                raise ValueError(message)

    @model_validator(mode="after")
    def _enforce_non_default_secrets(self) -> Self:
        """Purpose: Validate SECRET_KEY, POSTGRES_PASSWORD, and FIRST_SUPERUSER_PASSWORD are not defaults"""
        self._check_default_secret("SECRET_KEY", self.SECRET_KEY)
        self._check_default_secret("POSTGRES_PASSWORD", self.POSTGRES_PASSWORD)
        self._check_default_secret(
            "FIRST_SUPERUSER_PASSWORD", self.FIRST_SUPERUSER_PASSWORD
        )

        return self


settings = Settings()  # type: ignore
